package core.cc;

public class TermAndDefinition {
		String term;
		String definition;
		
		public TermAndDefinition(String term, String definition) {
			super();
			this.term = term;
			this.definition = definition;
		}

		public String getTerm() {
			return term;
		}
		
		public String getDefinition() {
			return definition;
		}
		
	
}
