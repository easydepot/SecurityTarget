package core.cc;

public class Identification {
	
		String title;
		String author;
		String version;
		String date;
		String sponsor;
		String cc_version;
		String cc_revision;
		String reference;
		
		public String getReference() {
			return reference;
		}
		public void setReference(String reference) {
			this.reference = reference;
		}
		public String getTitle() {
			return title;
		}
		public void setTitle(String title) {
			this.title = title;
		}
		public String getAuthor() {
			return author;
		}
		public void setAuthor(String author) {
			this.author = author;
		}
		public String getVersion() {
			return version;
		}
		public void setVersion(String version) {
			this.version = version;
		}
		public String getDate() {
			return date;
		}
		public void setDate(String date) {
			this.date = date;
		}
		public String getSponsor() {
			return sponsor;
		}
		public void setSponsor(String sponsor) {
			this.sponsor = sponsor;
		}
		public String getCc_version() {
			return cc_version;
		}
		public void setCc_version(String cc_version) {
			this.cc_version = cc_version;
		}
		public String getCc_revision() {
			return cc_revision;
		}
		public void setCc_revision(String cc_revision) {
			this.cc_revision = cc_revision;
		}
		
		

}
